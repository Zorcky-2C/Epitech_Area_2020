package com.example.spotifyapp.Services.FacebookModel;

public class FBFreinds {

    private String id;
    private String name;

    public FBFreinds(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
